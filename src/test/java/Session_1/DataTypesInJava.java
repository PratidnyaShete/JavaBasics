package Session_1;

public class DataTypesInJava {
public static void main(String[] args) {
	
	//Declare variable
	int week;
	int height;
	
	//Initialization of variable
	height = 50;
	
	//Byte : -127 to 128 | 1byte
	byte bredth = 4;
	byte mobileno = 127;
	
	//short : -32768 to 32767 | 2bytes
	short s1 = 500;
	short s2 = 6902;
	
	//Integer : 4 byte
	int i1 = 63215846;
	int i2 = 186345269;
	
	//Long : 8 byte
	long l1 = 165695632;
	long l2 = 6596645;
	
	//////Decimal point data type
	// 1. float  : 4 bytes
	float f1 = 125.6f;
	float f2 = (float)56.39;
	float f3 = 256;
	
	//double : 8 bytes
	double d1 = 45.395248962;
	
	
	System.out.println(bredth);
	System.out.println(f1);
	System.out.println(d1);
	
	//character: char - 2 byte | only single character can be stored 
	char c1 = 'a';
	char c2 = 'A';
	char symbol = '#';
	
	// String - can store combination of characters and its a class
	String password = "secure123";
	String empName = "Peter sam";
	
	System.out.println(c1);
	System.out.println(c2);
	System.out.println(symbol);
	System.out.println(password);
	System.out.println(empName);
	
	
	
	
	
	
	
	
	
	
	
	
}
}
